/**
 * Click to edit text view mode. Indicates if the view is in
 * edit mode or not.
 * <p/>
 * Created by fred on 26.12.16.
 */

public enum ClickToEditTextViewMode {
  /**
   * The view is editable at this point
   */
  EDIT,
  /**
   * The view is not editable at this point
   */
  TEXT
}
